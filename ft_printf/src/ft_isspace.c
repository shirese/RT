#include "libft.h"

int		ft_isspace(int c)
{
	unsigned int	i;

	i = c;
	if (i == '\t' || i == '\n' || i == '\v' || i == '\f' || i == '\r'
			|| i == ' ')
		return (1);
	return (0);
}
