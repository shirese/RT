#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	if (!alst || !*alst || !del)
		return ;
	del((*alst)->coord, (*alst)->coord_size);
	ft_memdel((void **)alst);
}
