static double		fade(double t)
{
	return (t * t * t * (t * (t * 6 - 15) + 10));
}

static double		grad(int hash, double x, double y, double z)
{
	int				h;
	double			u;
	double			v;

	h = hash & 15;
	u = h < 8 ? x : y;
	if (h < 4)
		v = y;
	else
		v = (h == 12 || h == 14) ? x : z;
	return (((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v));
}

static void			norme_noise(int xyz[3], int *aabb)
{
	aabb[0] = p[xyz[0]] + xyz[1];
	aabb[1] = p[aabb[0]] + xyz[2];
	aabb[2] = p[aabb[0] + 1] + xyz[2];
	aabb[3] = p[xyz[0] + 1] + xyz[1];
	aabb[4] = p[aabb[3]] + xyz[2];
	aabb[5] = p[aabb[3] + 1] + xyz[2];
}

double				noise(double x, double y, double z)
{
	int				xyz[3];
	double			uvw[3];
	int				aabb[6];

	xyz[0] = (int)floor(x) & 255;
	xyz[1] = (int)floor(y) & 255;
	xyz[2] = (int)floor(z) & 255;
	x -= floor(x);
	y -= floor(y);
	z -= floor(z);
	uvw[0] = fade(x);
	uvw[1] = fade(y);
	uvw[2] = fade(z);
	norme_noise(xyz, aabb);

	return (lerp(uvw[2], lerp(uvw[1], lerp(uvw[0], grad(p[aabb[1]], x, y, z), 
												grad(p[aabb[4]], x-1, y, z)),
									lerp(uvw[0], grad(p[aabb[2]], x, y-1, z), 
												grad(p[aabb[5]], x-1, y-1, z))),
						lerp(uvw[1], lerp(uvw[0], grad(p[aabb[1]+1], x, y, z-1 ), 
												grad(p[aabb[4]+1], x-1, y, z-1 )), 
									lerp(uvw[0], grad(p[aabb[2]+1], x, y-1, z-1 ),
												grad(p[aabb[5]+1], x-1, y-1, z-1 )))));
}

int					get_perlin_noise_pixel(int x, int y)
{
	int				c;
	double			n;

	n = noise(0.1 * x, 0.1 * y, 0.);
	n = n / 2 + 0.5;
	c = 255 * n;
	return (c);
}
